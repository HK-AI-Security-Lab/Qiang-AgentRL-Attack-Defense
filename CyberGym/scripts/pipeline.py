"""End-to-end CyberGym pipeline.

Steps:
  1. load inventory  -> static-baseline graph
  2. load capability table (translate first if missing or --translate is passed)
  3. inject VULN_EXPLOIT edges
  4. find_kill_paths
  5. chokepoints
  6. render attack-graph HTML (with overlay)
  7. write markdown report

usage:
  python -m scripts.pipeline                       # use default inventory + table
  python -m scripts.pipeline --translate           # rerun Base Model first
  python -m scripts.pipeline -i my.yaml -t cap.json
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

from dotenv import load_dotenv

from base_model.translate import translate_task
from harness.cut    import chokepoints, what_if_remove
from harness.graph  import build_graph, load_inventory, stats
from harness.inject import inject_capabilities
from harness.render import render_html
from harness.search import find_kill_paths


CYBERGYM_ROOT = Path(__file__).resolve().parent.parent

DEFAULTS = {
    "inventory":         CYBERGYM_ROOT / "inventory" / "sample.yaml",
    "tasks":             CYBERGYM_ROOT / "data"      / "sample_tasks.json",
    "capability_table":  CYBERGYM_ROOT / "data"      / "capability_table.json",
    "out_html":          CYBERGYM_ROOT / "out"       / "attack_graph.html",
    "out_report":        CYBERGYM_ROOT / "out"       / "report.md",
    "out_paths":         CYBERGYM_ROOT / "out"       / "kill_paths.json",
    "out_chokes":        CYBERGYM_ROOT / "out"       / "chokepoints.json",
}


def _ensure_capability_table(args: argparse.Namespace) -> Path:
    """If --translate or the cached table is missing, run Base Model now."""
    table_path = Path(args.capability_table)
    if args.translate or not table_path.exists():
        tasks = json.loads(Path(args.tasks).read_text(encoding="utf-8"))
        # Pick the same diverse subset translate.py uses
        from base_model.translate import _pick_diverse
        selected = _pick_diverse(tasks, args.translate_n)
        print(f"[pipeline] translating {len(selected)} task(s) via Base Model...")
        out = []
        for t in selected:
            entry = translate_task(t)
            print(f"  {entry['task_id']:<40} -> {entry['affected_node_type']:<10} "
                  f"({entry['source']})")
            out.append(entry)
        table_path.parent.mkdir(parents=True, exist_ok=True)
        table_path.write_text(json.dumps(out, indent=2, ensure_ascii=False), encoding="utf-8")
        print(f"[pipeline] wrote {table_path}")
    return table_path


def _path_dict(p: dict) -> dict:
    """Strip non-JSON-friendly fields from a kill-path entry for HTML/JSON."""
    return {
        "target":     p["target"],
        "path":       p["path"],
        "edges":      p["edges"],
        "uses_vuln":  p["uses_vuln"],
        "vuln_count": p["vuln_count"],
        "max_cvss":   p["max_cvss"],
        "depth":      p["depth"],
        "score":      p["score"],
    }


def _write_report(
    out: Path,
    *,
    inv_stats,
    paths,
    chokes,
    capability_table: list[dict],
    cap_edge_counts: dict[str, int],
) -> None:
    """Write a human-readable markdown summary."""
    applicable = [c for c in capability_table if cap_edge_counts.get(c["task_id"], 0) > 0]
    ghosts     = [c for c in capability_table if cap_edge_counts.get(c["task_id"], 0) == 0]

    lines: list[str] = []
    lines.append(f"# CyberGym Risk Report")
    lines.append("")
    lines.append(f"_deployment: **{inv_stats['deployment']}** -- generated by `scripts/pipeline.py`_")
    lines.append("")

    # ── Headline (the one number an SOC actually wants) ────────────────────
    lines.append("## TL;DR")
    lines.append("")
    if chokes and paths:
        top = chokes[0]
        pct = round(100 * top["paths_cut"] / len(paths))
        lines.append(
            f"> **Patch `{top['node']}` ({top['kind']}) first** — it sits on "
            f"**{top['paths_cut']} / {len(paths)}** live kill paths "
            f"(**{pct}%** attack-surface reduction)."
        )
    else:
        lines.append("> No kill paths found — the static topology + capabilities yield no reachable target.")
    lines.append("")
    lines.append(
        f"- Threat intel: **{len(applicable)} / {len(capability_table)}** ingested CVEs "
        f"actually land on our inventory ({len(ghosts)} not applicable)."
    )
    lines.append(
        f"- Live attack paths to critical assets: **{len(paths)}**."
    )
    if chokes:
        top_pct = chokes[0]["paths_cut"] / max(1, len(paths)) * 100
        lines.append(
            f"- Top-1 chokepoint covers **{top_pct:.0f}%** of paths."
        )
    lines.append("")

    # ── Inventory ─────────────────────────────────────────────────────────
    lines.append("## 1. Inventory at a glance")
    lines.append("")
    lines.append(f"- nodes: **{inv_stats['total_nodes']}**, edges: **{inv_stats['total_edges']}** (incl. VULN_EXPLOIT)")
    lines.append(f"- per layer: `{inv_stats['layer_counts']}`")
    lines.append(f"- per edge type: `{inv_stats['edge_counts']}`")
    lines.append("")

    # ── Capability table ─────────────────────────────────────────────────
    lines.append("## 2. Threat intel coverage")
    lines.append("")
    lines.append(
        f"Base Model produced **{len(capability_table)}** capability entries. "
        f"**{len(applicable)}** of them found a matching inventory node and produced "
        f"VULN_EXPLOIT edges; **{len(ghosts)}** are recorded but have no impact on this deployment."
    )
    lines.append("")
    if applicable:
        lines.append("### Applicable CVEs")
        lines.append("")
        lines.append("| CVE / task | type | cvss | post_condition | edges | rationale |")
        lines.append("|---|---|---:|---|---:|---|")
        for c in applicable:
            cve = c.get("cve_id") or c["task_id"].split(":")[-1]
            post = ", ".join(c.get("post_condition") or [])
            n = cap_edge_counts.get(c["task_id"], 0)
            rat = (c.get("rationale") or "").replace("|", "\\|")[:120]
            lines.append(
                f"| `{cve}` | {c.get('affected_node_type')} | {c.get('cvss')} | {post} | {n} | {rat} |"
            )
        lines.append("")
    if ghosts:
        lines.append("### Not applicable (no inventory match)")
        lines.append("")
        for c in ghosts:
            cve = c.get("cve_id") or c["task_id"].split(":")[-1]
            hint = (c.get("match_hints") or {}).get("project_name") or "?"
            lines.append(f"- `{cve}` ({c.get('affected_node_type')}) — looks for project `{hint}`, not in our inventory")
        lines.append("")

    # ── Kill paths ────────────────────────────────────────────────────────
    lines.append("## 3. Top kill paths")
    lines.append("")
    if not paths:
        lines.append("_no kill paths found._")
    else:
        lines.append("Sorted by score = max(CVSS) x criticality x vuln_factor - depth_penalty.")
        lines.append("")
        lines.append("| score | cvss | depth | target | path |")
        lines.append("|---:|---:|---:|---|---|")
        for p in paths[:12]:
            chain = " -> ".join(p["path"])
            lines.append(
                f"| {p['score']} | {p['max_cvss']:.1f} | {p['depth']} | "
                f"`{p['target']}` | `{chain}` |"
            )
    lines.append("")

    # ── Chokepoints ───────────────────────────────────────────────────────
    lines.append("## 4. Recommended chokepoints")
    lines.append("")
    if not chokes:
        lines.append("_no chokepoints — paths don't share intermediate nodes._")
    else:
        lines.append("Patch / harden these nodes first. `paths_cut` is how many of the kill paths above pass through this node.")
        lines.append("")
        lines.append("| rank | node | kind | paths_cut | score_cut | betweenness | blend |")
        lines.append("|---:|---|---|---:|---:|---:|---:|")
        for i, cp in enumerate(chokes, start=1):
            lines.append(
                f"| {i} | `{cp['node']}` | {cp['kind']} | "
                f"{cp['paths_cut']} | {cp['score_sum']} | {cp['betweenness']} | {cp['blend']} |"
            )
    lines.append("")

    # ── Method ────────────────────────────────────────────────────────────
    lines.append("## 5. Method note")
    lines.append("")
    lines.append(
        "1. Static baseline (NETWORK_REACH / IAM_BINDING / DATA_FLOW / RUNS_AS / DEPENDS_ON / BELONGS_TO) "
        "is loaded from `inventory/sample.yaml`."
    )
    lines.append(
        "2. Per-CVE capability tables are produced by the **Base Model** "
        "(`base_model/translate.py`) — an LLM constrained to a fixed JSON schema "
        "with vocabulary `affected_node_type / pre_condition / post_condition / cvss / "
        "exploit_maturity / match_hints`."
    )
    lines.append(
        "3. The **Harness** matches each capability to inventory nodes by `affected_node_type` "
        "and `match_hints.project_name`, then injects directional `VULN_EXPLOIT` edges. "
        "The injection algorithm is purely deterministic; the LLM never participates."
    )
    lines.append(
        "4. Kill paths are simple paths from `internet` to any critical asset, "
        "depth <= 6, walking `NETWORK_REACH | IAM_BINDING | RUNS_AS | DEPENDS_ON | VULN_EXPLOIT`."
    )
    lines.append(
        "5. Chokepoints blend path-cut count (70%) with betweenness centrality (30%) "
        "to surface the single nodes whose patch buys the most attack-surface reduction."
    )
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines), encoding="utf-8")


def main() -> int:
    load_dotenv(CYBERGYM_ROOT.parent / ".env")

    ap = argparse.ArgumentParser(description="CyberGym attack-graph pipeline")
    ap.add_argument("-i", "--inventory",         default=str(DEFAULTS["inventory"]))
    ap.add_argument("-t", "--capability_table",  default=str(DEFAULTS["capability_table"]))
    ap.add_argument(      "--tasks",             default=str(DEFAULTS["tasks"]))
    ap.add_argument(      "--translate",         action="store_true",
                          help="re-run Base Model on tasks before pipeline")
    ap.add_argument(      "--translate-n",       type=int, default=8)
    ap.add_argument(      "--max-depth",         type=int, default=6)
    ap.add_argument(      "--top-paths",         type=int, default=3,
                          help="paths kept per target before global ranking")
    ap.add_argument(      "--top-chokes",        type=int, default=5)
    ap.add_argument(      "--out-html",          default=str(DEFAULTS["out_html"]))
    ap.add_argument(      "--out-report",        default=str(DEFAULTS["out_report"]))
    ap.add_argument(      "--out-paths",         default=str(DEFAULTS["out_paths"]))
    ap.add_argument(      "--out-chokes",        default=str(DEFAULTS["out_chokes"]))
    args = ap.parse_args()

    print("[pipeline] step 1: load inventory")
    g = build_graph(load_inventory(args.inventory))
    s = stats(g)
    print(f"  baseline: nodes={s['total_nodes']} edges={s['total_edges']} "
          f"layers={s['layer_counts']}")

    print("\n[pipeline] step 2: capability table (Base Model)")
    table_path = _ensure_capability_table(args)
    table = json.loads(Path(table_path).read_text(encoding="utf-8"))
    print(f"  using {len(table)} capabilities from {table_path}")

    print("\n[pipeline] step 3: inject VULN_EXPLOIT edges")
    inject_capabilities(g, table)
    s2 = stats(g)
    print(f"  after inject: edges={s2['total_edges']} types={s2['edge_counts']}")

    # Per-capability edge counts: how many CVEs actually landed on our assets?
    cap_edge_counts: dict[str, int] = {}
    for u, v, attrs in g.edges(data=True):
        if attrs.get("type") != "VULN_EXPLOIT" or u == v:
            continue
        key = attrs.get("task_id") or attrs.get("cve_id")
        if key:
            cap_edge_counts[key] = cap_edge_counts.get(key, 0) + 1
    applicable = sum(1 for c in table if cap_edge_counts.get(c["task_id"], 0) > 0)
    ghost      = len(table) - applicable
    print(f"  CVE coverage: {applicable}/{len(table)} applicable, {ghost} ghost (no inventory match)")

    print("\n[pipeline] step 4: enumerate kill paths")
    paths = find_kill_paths(g, max_depth=args.max_depth, max_paths_per_target=args.top_paths)
    print(f"  {len(paths)} paths from internet to critical assets")

    print("\n[pipeline] step 5: chokepoint analysis")
    chokes = chokepoints(g, paths, top_k=args.top_chokes)
    for cp in chokes:
        sim = what_if_remove(g, cp["node"], paths)
        print(f"  {cp['node']:<22} ({cp['kind']:<10})  cuts {sim['paths_cut']:>2}/{sim['paths_total']}  "
              f"blend={cp['blend']}")

    print("\n[pipeline] step 6: write artefacts")
    paths_compact = [_path_dict(p) for p in paths]
    Path(args.out_paths ).parent.mkdir(parents=True, exist_ok=True)
    Path(args.out_chokes).parent.mkdir(parents=True, exist_ok=True)
    Path(args.out_paths ).write_text(json.dumps(paths_compact, indent=2, ensure_ascii=False), encoding="utf-8")
    Path(args.out_chokes).write_text(json.dumps(chokes,        indent=2, ensure_ascii=False), encoding="utf-8")

    out_html = render_html(
        g, args.out_html,
        kill_paths=paths_compact,
        chokepoints=chokes,
        capability_table=table,
    )
    print(f"  HTML:   {out_html}")

    _write_report(
        Path(args.out_report),
        inv_stats=s2, paths=paths, chokes=chokes,
        capability_table=table,
        cap_edge_counts=cap_edge_counts,
    )
    print(f"  report: {args.out_report}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
